#include "../../inc/minishell.h"

int	env(char **envl)
{
	int i;

	i = 0;
	while (envl[i])
	{
		ft_putstr_fd(envl[i], 1);
		ft_putchar_fd('\n', 1);
		i++;
	}
	return (0);
}